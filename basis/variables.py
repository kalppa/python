#变量名区分大小写

#命名规范
    #1  见名知意    username, password
    #2  小驼峰式    userName    第一个单词首字母小写，其它单词首字母大写
    #3  大驼峰式    UserName    全部首字母大写
    #4  下划线式    user_name   单词间用下划写连接


a = 'ABC'
b = 'XYZ'
# print里面一个逗号就会有一个空格，\n后面有个逗号所以换行之后就有空格了
print(a,'\n',b,)

#使用sep='\n'表示在变量之间使用换行符隔开
print(a,b,sep='\n')









